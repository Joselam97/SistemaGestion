//operadors

//suma
let a = 2, b = 3;
let z = a + b;
console.log(z)

//resta
z = a - b;
console.log(z)

//division
z = a / b;
console.log(z)

//multiplicacion
z = a * b;
console.log(z)

//residuo de division
z = a % b;
console.log(z)

//potencia
z = a ** b;
console.log(z)

//post-incremento: se le suma 1
z = a++; 
console.log(z)

//pre-incremento: se le suma 1
z = ++a;
console.log(z)

//pre-decremento: se le resta 1
z = --a;
console.log(z)